#IMPORTS 3
import math as m

val = 3
sqrtVal = m.sqrt(val)
print(sqrtVal ** 2)

expVal = m.exp(val)
print(expVal)

factVal = m.factorial(val)
print(f"Factorial of 3 is {factVal}")

sinVal = m.sin(val)
print(f"Sin of 3 is {sinVal} in rad")

val = 3.14
floorVal = m.floor(val)
print(f"Number rounded to {floorVal}")

ceilingVal = m.ceil(val)
print(f"Number is rounded up to {ceilingVal}")

